public class TrigCalc {
    /**
     *The logorithmic methodology
     */
    public double sine(double value){return Math.sin(value);}
    public double coSine(double value){ return Math.cos(value) ;}
    public double tangent(double value){ return Math.tan(value) ;}
    public double invSine(double value){return (Math.asin(value));}
    public double invCosine(double value){ return (Math.acos(value)) ;}
    public double invTangent(double value){ return (Math.atan(value)) ;}
}
